public class SwitchStatement {
    public static void main(String[]args){
        int passWord=5;
        switch(passWord){
            case 6:
                System.out.println("the figures is to much");
                break;
            case 7:
                System.out.println("please reduce two figures");
                break;
            case 5:
                System.out.println("correct");
                break;
            default:
                System.out.println("error");
                break;
        }


    }
}
